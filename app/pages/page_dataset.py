class PillClassTree(QWidget):
    """Tree showing data/<main>/<sub> with image counts.

    Parameters
    ----------
    show_add : bool
        Show the "Add Class" button (default True). Set to False for
        Backbone/Defect tabs where classes are generated by pill_process.
    show_rename : bool
        Show "Rename" button (default False). Enabled for Data tab.
    """

    class_selected = pyqtSignal(str, str)   # (main_class, sub_class)

    def __init__(self, root_dir: str, parent=None, *, show_add: bool = True, show_rename: bool = False):
        super().__init__(parent)
        self._root = Path(root_dir)
        self._show_add = show_add
        self._show_rename = show_rename

        lay = QVBoxLayout(self)
        lay.setContentsMargins(0, 0, 0, 0)

        title = QLabel("Pill Classes")
        title.setStyleSheet("font-weight:bold; font-size:13px; padding:2px;")
        lay.addWidget(title)

        self._search = QLineEdit()
        self._search.setPlaceholderText("Search")
        self._search.textChanged.connect(self._filter)
        lay.addWidget(self._search)

        self._tree = QTreeWidget()
        self._tree.setHeaderHidden(True)
        self._tree.itemClicked.connect(self._on_click)
        lay.addWidget(self._tree)

        btn_row = QHBoxLayout()
        if self._show_add:
            self._btn_add = QPushButton("Add Class")
            btn_row.addWidget(self._btn_add)
            self._btn_add.clicked.connect(self._add_class)

        if self._show_rename:
            self._btn_rename = QPushButton("Rename")
            btn_row.addWidget(self._btn_rename)
            self._btn_rename.clicked.connect(self._rename_class)

        self._btn_del = QPushButton("Delete Class")
        self._btn_del.setStyleSheet("color:red;")
        btn_row.addWidget(self._btn_del)
        self._btn_del.clicked.connect(self._delete_class)

        lay.addLayout(btn_row)

        self.refresh()

    def refresh(self):
        self._tree.clear()
        if not self._root.exists():
            return
        for main_dir in sorted(self._root.iterdir()):
            if not main_dir.is_dir():
                continue
            main_item = QTreeWidgetItem([main_dir.name])
            main_item.setData(0, Qt.ItemDataRole.UserRole, ("main", main_dir.name, ""))
            self._tree.addTopLevelItem(main_item)
            for sub_dir in sorted(main_dir.iterdir()):
                if not sub_dir.is_dir():
                    continue
                n_images = self._count_images(sub_dir)
                sub_item = QTreeWidgetItem([f"{sub_dir.name}    {n_images}"])
                sub_item.setData(0, Qt.ItemDataRole.UserRole, ("sub", main_dir.name, sub_dir.name))
                main_item.addChild(sub_item)

    def _count_images(self, d: Path) -> int:
        count = 0
        for f in d.rglob("*"):
            if f.is_file() and f.suffix.lower() in IMAGE_EXTS:
                count += 1
        return count

    def _filter(self, text: str):
        text = text.lower()
        for i in range(self._tree.topLevelItemCount()):
            item = self._tree.topLevelItem(i)
            match = text in item.text(0).lower()
            child_match = False
            for j in range(item.childCount()):
                ch = item.child(j)
                cm = text in ch.text(0).lower()
                ch.setHidden(not cm and not match)
                if cm:
                    child_match = True
            item.setHidden(not match and not child_match)

    def _on_click(self, item: QTreeWidgetItem, col: int):
        data = item.data(0, Qt.ItemDataRole.UserRole)
        if data and data[0] == "sub":
            self.class_selected.emit(data[1], data[2])
        elif data and data[0] == "main":
            if item.childCount() > 0:
                ch = item.child(0)
                ch_data = ch.data(0, Qt.ItemDataRole.UserRole)
                if ch_data:
                    self.class_selected.emit(ch_data[1], ch_data[2])

    def _add_class(self):
        name, ok = QInputDialog.getText(self, "Add Class", "Main class name:")
        if not ok or not name.strip():
            return
        name = name.strip()
        sub, ok2 = QInputDialog.getText(self, "Add Sub-class", "Sub-class name:", text=name)
        if not ok2 or not sub.strip():
            sub = name
        new_dir = self._root / name / sub.strip()
        new_dir.mkdir(parents=True, exist_ok=True)
        self.refresh()

    def _rename_class(self):
        """Rename the currently selected main-class or sub-class folder."""
        sel = self._tree.currentItem()
        if sel is None:
            QMessageBox.warning(self, "Rename", "Select a class to rename.")
            return
        data = sel.data(0, Qt.ItemDataRole.UserRole)
        if data is None:
            return

        if data[0] == "main":
            old_path = self._root / data[1]
            old_name = data[1]
        else:
            old_path = self._root / data[1] / data[2]
            old_name = data[2]

        new_name, ok = QInputDialog.getText(self, "Rename", f"New name for '{old_name}':", text=old_name)
        if not ok or not new_name.strip() or new_name.strip() == old_name:
            return

        new_path = old_path.parent / new_name.strip()
        if new_path.exists():
            QMessageBox.warning(self, "Rename", f"'{new_name.strip()}' already exists.")
            return
        try:
            old_path.rename(new_path)
        except Exception as e:
            QMessageBox.critical(self, "Error", str(e))
            return
        self.refresh()

    def _delete_class(self):
        sel = self._tree.currentItem()
        if sel is None:
            return
        data = sel.data(0, Qt.ItemDataRole.UserRole)
        if data is None:
            return
        if data[0] == "main":
            target = self._root / data[1]
        else:
            target = self._root / data[1] / data[2]

        ans = QMessageBox.question(
            self, "Delete", f"Delete '{target.name}' and all contents?",
            QMessageBox.StandardButton.Yes | QMessageBox.StandardButton.No,
        )
        if ans == QMessageBox.StandardButton.Yes:
            shutil.rmtree(target, ignore_errors=True)
            self.refresh()


