    # ── tree with checkboxes ─────────────────────────────
    def _load_tree(self):
        self._tree.clear()
        root = Path("data_train_defection")
        if not root.exists():
            return
        for main_dir in sorted(root.iterdir()):
            if not main_dir.is_dir():
                continue
            main_item = QTreeWidgetItem([main_dir.name, "", ""])
            main_item.setData(0, Qt.ItemDataRole.UserRole, ("main", main_dir.name, ""))
            self._tree.addTopLevelItem(main_item)
            for sub_dir in sorted(main_dir.iterdir()):
                if not sub_dir.is_dir():
                    continue
                n = sum(1 for f in sub_dir.rglob("*") if f.is_file() and f.suffix.lower() in IMAGE_EXTS)
                sub_item = QTreeWidgetItem([sub_dir.name, str(n)])
                sub_item.setData(0, Qt.ItemDataRole.UserRole, ("sub", main_dir.name, sub_dir.name))
                sub_item.setCheckState(2, Qt.CheckState.Unchecked)
                main_item.addChild(sub_item)

    def _filter_tree(self, text: str):
        text = text.lower()
        for i in range(self._tree.topLevelItemCount()):
            item = self._tree.topLevelItem(i)
            match = text in item.text(0).lower()
            child_match = False
            for j in range(item.childCount()):
                ch = item.child(j)
                cm = text in ch.text(0).lower()
                ch.setHidden(not cm and not match)
                if cm:
                    child_match = True
            item.setHidden(not match and not child_match)

    def _on_tree_click(self, item: QTreeWidgetItem, col: int):
        data = item.data(0, Qt.ItemDataRole.UserRole)
        if not data:
            return
        if data[0] == "sub":
            self._current_main = data[1]
            self._current_sub = data[2]
            img_dir = Path("data_train_defection") / data[1] / data[2]
            paths = sorted(
                str(f) for f in img_dir.rglob("*")
                if f.is_file() and f.suffix.lower() in IMAGE_EXTS
            )
            self._grid.load_paths(paths)

    def _get_checked_main_classes(self) -> list[str]:
        """Return deduplicated list of main_class names whose sub-classes are checked."""
        mains: set[str] = set()
        for i in range(self._tree.topLevelItemCount()):
            item = self._tree.topLevelItem(i)
            for j in range(item.childCount()):
                ch = item.child(j)
                if ch.checkState(2) == Qt.CheckState.Checked:
                    data = ch.data(0, Qt.ItemDataRole.UserRole)
                    if data:
                        mains.add(data[1])   # data[1] is main_class
        return sorted(mains)

    def _delete_class(self):
        sel = self._tree.currentItem()
        if sel is None:
            return
        data = sel.data(0, Qt.ItemDataRole.UserRole)
        if not data:
            return
        if data[0] == "main":
            target = Path("data_train_defection") / data[1]
        else:
            target = Path("data_train_defection") / data[1] / data[2]
        ans = QMessageBox.question(
            self, "Delete", f"Delete '{target.name}' and all contents?",
            QMessageBox.StandardButton.Yes | QMessageBox.StandardButton.No,
        )
        if ans == QMessageBox.StandardButton.Yes:
            shutil.rmtree(target, ignore_errors=True)
            self._load_tree()

    def _capture_pills(self):
        """Save the last segmented pill crops from PillCameraWidget."""
        crops = self._pill_cam.last_crops
        if not crops:
            QMessageBox.warning(self, "Capture", "No segmented pills available. Start camera first.")
            return
        if not self._current_main or not self._current_sub:
            QMessageBox.warning(self, "Capture", "Select a class first.")
            return
        out_dir = Path("data_train_defection") / self._current_main / self._current_sub / "train" / "good"
        out_dir.mkdir(parents=True, exist_ok=True)
        for i, crop in enumerate(crops):
            ts = datetime.now().strftime("%Y%m%d_%H%M%S_%f")
            save_path = str(out_dir / f"pill_{ts}_{i:02d}.png")
            self._grid.add_cv_image(crop, save_path)

    def _browse_images(self):
        if not self._current_main or not self._current_sub:
            QMessageBox.warning(self, "Browse", "Select a class first.")
            return
        files, _ = QFileDialog.getOpenFileNames(
            self, "Select Images", "",
            "Images (*.png *.jpg *.jpeg *.bmp *.webp);;All Files (*)",
        )
        if not files:
            return
        out_dir = Path("data_train_defection") / self._current_main / self._current_sub / "train" / "good"
        out_dir.mkdir(parents=True, exist_ok=True)
        for f in files:
            src = Path(f)
            dst = out_dir / src.name
            if not dst.exists():
                shutil.copy2(src, dst)
        sel = self._tree.currentItem()
        if sel:
            self._on_tree_click(sel, 0)
        self._load_tree()

    def _run_train(self):
        """Run ``main.py train --classes ...`` for selected main classes."""
        if self._runner and self._runner.isRunning():
            QMessageBox.information(self, "Training", "Training already in progress.")
            return

        checked = self._get_checked_main_classes()
        if not checked:
            QMessageBox.warning(self, "Train", "Check at least one class in the tree before training.")
            return

        cmd = [sys.executable, "main.py", "train", "--classes"] + checked
        self._runner = ScriptRunner(cmd)
        self._runner.output.connect(self._on_train_output)
        self._runner.finished_signal.connect(self._on_train_done)
        self._btn_train.setEnabled(False)
        self._btn_train.setText("Training...")
        self._progress.setVisible(True)
        self._progress.setRange(0, 0)
        self._runner.start()

    def _on_train_output(self, line: str):
        self._lbl_status.setText(line[-120:])

    def _on_train_done(self, code: int):
        self._btn_train.setEnabled(True)
        self._btn_train.setText("Train Defect Select")
        self._progress.setVisible(False)
        if code == 0:
            QMessageBox.information(self, "Done", "Defect training completed!")
        else:
            QMessageBox.warning(self, "Error",
                                f"Training failed (exit code {code}).\n{self._lbl_status.text()}")

    def cleanup(self):
        self._pill_cam.stop()


